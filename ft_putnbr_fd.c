#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	*nb;

	nb = ft_itoa(n);
	if (n == 0)
		write(fd, "0", 1);
	if (nb)
		ft_putstr_fd(nb, fd);
}
